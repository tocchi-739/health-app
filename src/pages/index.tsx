import type { NextPage } from "next";
import Head from "next/head";
import styles from "../../styles/Home.module.css";
import { InputArea } from "../components/InputArea";
import { DisPlayArea } from "../components/DisplayArea";
import { Toaster } from "react-hot-toast";
import { GraphArea } from "../components/GraphArea";
import { useEffect, useState } from "react";
import {
  DocumentData,
  collection,
  getFirestore,
  onSnapshot,
} from "firebase/firestore";
import { app } from "../firebase/firebase";

const db = getFirestore(app);
const Home: NextPage = () => {
  const [displayChangeFlag, setDisplayChangeFlag] = useState(true);

  const [firebaseData, setFirebaseData] = useState<DocumentData>();

  useEffect(() => {
    // Firestoreのデータ監視を設定
    const unsubscribe = onSnapshot(
      collection(db, "health-data"),
      (snapshot) => {
        const dataList = snapshot.docs.map((doc) => {
          const data = doc.data();
          return {
            id: doc.id,
            date: data.date,
            weight: data.weight,
            fatPercent: data.fatPercent,
            visceralFatLevel: data.visceralFatLevel,
            bmi: data.bmi,
          };
        });
        setFirebaseData(dataList);
      }
    );

    // コンポーネントのアンマウント時にデータ監視を停止
    return () => unsubscribe();
  }, []);
  return (
    <>
      <Head>
        <title>Health App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className={styles.container}>
        <header>
          <h1 className="text-xl p-2 text-cyan-900">Health App!</h1>
        </header>
        <main className={styles.main}>
          <InputArea />
          <div className="flex mt-6 bg-cyan-900 text-white">
            <button
              className="border-white border"
              onClick={() =>
                displayChangeFlag
                  ? displayChangeFlag
                  : setDisplayChangeFlag(!displayChangeFlag)
              }
            >
              一覧
            </button>
            <button
              className="border-white border"
              onClick={() =>
                displayChangeFlag
                  ? setDisplayChangeFlag(!displayChangeFlag)
                  : displayChangeFlag
              }
            >
              グラフ
            </button>
          </div>
          {displayChangeFlag ? (
            <DisPlayArea db={db} firebaseData={firebaseData} />
          ) : (
            <GraphArea firebaseData={firebaseData} />
          )}
        </main>
        <footer></footer>
      </div>
      <Toaster />
    </>
  );
};

export default Home;
